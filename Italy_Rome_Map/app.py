@app.route("/")
def home():
    return render_template("index.html")

@app.route("/<city>")
def city_page(city):
    if city not in CITIES:
        return redirect("/")
    return render_template("index.html")

@app.route("/api/cities")
def get_cities():
    cities_list = []
    for city_id, city_data in CITIES.items():
        cities_list.append({
            "id": city_id,
            "name": city_data["name"],
            "country": city_data["country"],
            "description": city_data["description"],
            "image": city_data["image"],
            "count": len(city_data["restaurants"])
        })
    return jsonify({"cities": cities_list})

@app.route("/api/cities/<city_id>")
def get_city(city_id):
    if city_id not in CITIES:
        return jsonify({"error": "City not found"}), 404
    city = CITIES[city_id]
    return jsonify({
        "city": {
            "id": city_id,
            "name": city["name"],
            "center": city["center"],
            "zoom": city["zoom"]
        },
        "restaurants": city["restaurants"],
        "categories": city.get("categories", ROME_CATEGORIES)
    })

@app.route("/api/restaurants")
def get_restaurants():
    return jsonify({
        "restaurants": CITIES["rome"]["restaurants"],
        "categories": ROME_CATEGORIES
    })

